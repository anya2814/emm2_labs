# –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ —Å.–≤. ùúÄùëò‚àÜ —Ä–∞–≤–Ω–æ ‚àö ‚àÜ = 0.01) –∏ ùëò = 0,1,...,10^3

