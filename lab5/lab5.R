# 5. –†–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å –ø—Ä–æ—Ü–µ—Å—Å (9) –ø—Ä–∏ —Å–ª–µ–¥—É—é—â–∏—Ö –≤—Ö–æ–¥–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏—è—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤:
# ùëÜ0 = 1, ùëé = 0.5, ùúé = 0.9,
# ‚àö ‚àÜ = 0.01, ùëò = 0,1,...,103.

S = array(dim=1001)
S[1] = 1; a = 0.5; sig = 0.9

# d = 0.0001
# i = 0:1000
# kd = d*i
B = array(dim=1001)
B[1] = 0

for(i in 2:1001) {
  B[i] = B[i-1] + rnorm(1,0,0.01) 
  S[i] = S[1]*exp((a-sig^2/2)*(i)*d+sig*B[i])
}
plot(kd,S,type='l')

# 6. –ü–æ—Å—Ç—Ä–æ–∏—Ç—å 200 —Ä–µ–∞–ª–∏–∑–∞—Ü–∏–π –ø—Ä–æ—Ü–µ—Å—Å–∞ (10) –Ω–∞ –æ–¥–Ω–æ–º –≥—Ä–∞—Ñ–∏–∫–µ.   
plot(kd,S,type='l',ylim=c(0,3))

j = 0 
repeat {
  for(i in 2:1001) {
    B[i] = B[i-1] + rnorm(1,0,0.01) 
    S[i] = S[1]*exp((a-sig^2/2)*(i)*d+sig*B[i])
  } 
  lines(kd,S,type='l')
  j = j + 1
  if(j == 199) break
}            

